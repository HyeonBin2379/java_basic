package javabasic_01.day02;

public class VariableEx01 {

    public static void main(String[] args) {
        // 1. 정수형 변수 value를 선언
        int value;

        // 2. 변수 value에 20을 저장
        value = 20;

        // 3. 변수 result에 value에 30을 더한 값을 저장
        int result = value + 30;

        // 4. 변수 result의 값을 출력
        System.out.println(result);
    }
}
